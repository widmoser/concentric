let nextIndex = 0;

/**
 * Returns at most {@code count} unused names.
 *
 * If not enough names are available, only the remaining names are returned. If all names are exhausted, the pointer
 * is reset and the next call to this function will reuse old names from the beginning of the list.
 */
function getAvailableNames(count) {
    const availableCount = Math.min(count, names.length - nextIndex);
    const result = names.slice(nextIndex, nextIndex + availableCount);
    nextIndex += availableCount;
    return result;
}

/**
 * Returns an array of names not yet used.
 *
 * If the count exceeds the number of names available, the oldest names are being reused.
 *
 * @param count The size of the array to be returned.
 */
function getNames(count) {
    let freshNames = [];
    while (freshNames.length < count) {
        freshNames = freshNames.concat(getAvailableNames(count - freshNames.length));
    }
    return freshNames;
}